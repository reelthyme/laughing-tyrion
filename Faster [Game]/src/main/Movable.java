package main;

public class Movable extends Drawable {
	
	private float vx;
	private float vy;
	private float ax;
	private float ay;
	
	public void onEnterFrame(){
		vy+=ay;
		vx+=ax;
		x+=vx;
		y+=vy;
	}

	public float getVx() {
		return vx;
	}

	public void setVx(float vx) {
		this.vx = vx;
	}

	public float getVy() {
		return vy;
	}

	public void setVy(float vy) {
		this.vy = vy;
	}

	public float getAx() {
		return ax;
	}

	public void setAx(float ax) {
		this.ax = ax;
	}

	public float getAy() {
		return ay;
	}

	public void setAy(float ay) {
		this.ay = ay;
	}
	
	
}
